public class BaiHat {
    private int maDanhSach;
    private String tenBaiHat;
    private String hinhBaiHat;
    private String tenCaSy;
    private String linkBaiHat;

    public int getMaDanhSach() {
        return maDanhSach;
    }

    public void setMaDanhSach(int maDanhSach) {
        this.maDanhSach = maDanhSach;
    }

    public String getTenBaiHat() {
        return tenBaiHat;
    }

    public void setTenBaiHat(String tenBaiHat) {
        this.tenBaiHat = tenBaiHat;
    }

    public String getHinhBaiHat() {
        return hinhBaiHat;
    }

    public void setHinhBaiHat(String hinhBaiHat) {
        this.hinhBaiHat = hinhBaiHat;
    }

    public String getTenCaSy() {
        return tenCaSy;
    }

    public void setTenCaSy(String tenCaSy) {
        this.tenCaSy = tenCaSy;
    }

    public String getLinkBaiHat() {
        return linkBaiHat;
    }

    public void setLinkBaiHat(String linkBaiHat) {
        this.linkBaiHat = linkBaiHat;
    }
}
